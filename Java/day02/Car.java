public class Car {
	private String carName;
	private String carBrand;

	public Car(String carName, String carBrand) {
		this.carName = carName;
		this.carBrand = carBrand;
	}

	public String getCarName() {
		return carName;
	}

	public void setCarName(String carName) {
		this.carName = carName;
	}
}
